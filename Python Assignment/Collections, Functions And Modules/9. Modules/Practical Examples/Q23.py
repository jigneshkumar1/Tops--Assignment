#  23) Write a Python program to demonstrate the use of functions from  the math module. 

import math



# Using sqrt() 
number = 5
sqrt_result = math.sqrt(number)
print(f"The square root of {number} is: {sqrt_result}")

# Using floor() 
float_number_two = 10.5
floor_result = math.floor(float_number_two)
print(f"The floor value of {float_number_two} is: {floor_result}")

# Using factorial()
n = 5
factorial_result = math.factorial(n)
print(f"Factorial of {n} is {factorial_result}")

# Using math.pi 
print(f"Value of pi is {math.pi}")

