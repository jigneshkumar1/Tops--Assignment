# 14) Write a Python program to access values using keys from a dictionary.

# Define a dictionary
my_dict = {
    "name": "jignesh",
    "age": 24,
    "city": "Rajasthan",
    "job": "It"
}

# Access values using keys
print("Name:", my_dict["name"])   
print("Age:", my_dict["age"])     
print("City:", my_dict["city"])   
print("Job:", my_dict["job"])
